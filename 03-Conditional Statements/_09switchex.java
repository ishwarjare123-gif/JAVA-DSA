public class switchex{
    public static void main(String[] args) {
        
        char ch ='b';

        switch(ch){

            case 'a':System.out.println("samosa");
                      break;
            case 'b':System.out.println("mango");
                       break;
            case 'c':System.out.println("banana");    
                        break;
             default :System.out.println("not availabe");  
        }
    }
    
}
