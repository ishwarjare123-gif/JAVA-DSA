public class relational {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 10;
        System.out.println((a==b));    //equal to
        System.out.println((a!=b));   // not equal to

        int c = 13;
        int d = 14;
        System.out.println((c<d));      //smaller than
        System.out.println((c>d));      // greter than
    
        int f =24;
        int g=24;
        System.out.println((f>=g));    //greater than or equal to
        System.out.println((f<=g));    //smaller than or equal to
    
    }
}
