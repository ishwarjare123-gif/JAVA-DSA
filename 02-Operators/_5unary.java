public class unary {
    public static void main(String[] args) {
        
        //postincrement for +
        int a =10;
        int b = a++;

        System.out.println(a);
        System.out.println(b);

        //preincrement for +
        int c = 12;
        int d = ++c;
        System.out.println(c);
        System.out.println(d);

        //prencrement for -
        int e = 15;
        int f = --e;
        System.out.println(e);
        System.out.println(f);

        //postincrement for -
        int g =14;
        int h = g--;
        System.out.println(g);
        System.out.println(h);
    }
    
}
