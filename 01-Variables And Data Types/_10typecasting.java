
import java.util.Scanner;

public class typecasting {
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        float a = 40.20f;
        int a2 =(int) a;
        System.out.println(a2);

        //also ch can convert into int
        char ch = 'a';
        int ch2 = ch;
        System.out.println(ch2);
    }
    
}
